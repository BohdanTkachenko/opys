use std::collections::BTreeMap;

use crate::doc::Doc;
use crate::error::{usage, Result};
use crate::project::Project;
use crate::refs;
use crate::Ctx;

pub fn run(ctx: &Ctx) -> Result<()> {
    let prj = ctx.open()?;
    // Drive the full sync (reconcile references + linkify + regenerate views).
    crate::commands::sync::run(&prj)?;
    println!("synced {}", prj.base.join("INDEX.md").display());
    Ok(())
}

/// Regenerate the unified `INDEX.md` (grouped by type) and the `views/`
/// (`by-tag/`, `status/`, `by-reference/`). Returns the number of view files
/// written. Errors if any document fails to parse.
pub fn regenerate(prj: &Project) -> Result<usize> {
    let (mut docs, errors) = prj.load_docs();
    if !errors.is_empty() {
        return Err(usage("fix parse errors first (run verify)"));
    }
    docs.sort_by(|a, b| {
        refs::id_number(a.id().unwrap_or("")).cmp(&refs::id_number(b.id().unwrap_or("")))
    });

    // One INDEX.md at the base, grouped by type (config order).
    let pcfg = &prj.pcfg;
    let mut lines = vec![
        "# Index".to_string(),
        String::new(),
        "Generated by opys — do not edit.".to_string(),
        String::new(),
    ];
    for tname in pcfg.types.keys() {
        let group: Vec<&Doc> = docs
            .iter()
            .filter(|d| d.id().and_then(|id| pcfg.type_name_for_id(id)) == Some(tname.as_str()))
            .collect();
        if group.is_empty() {
            continue;
        }
        lines.push(format!("## {tname}"));
        lines.push(String::new());
        for d in group {
            lines.push(index_line(d));
        }
        lines.push(String::new());
    }
    std::fs::write(prj.base.join("INDEX.md"), lines.join("\n") + "\n")?;

    // views/: wipe and regenerate each bucket dir.
    let views = &prj.views_dir;
    for sub in ["by-tag", "status", "by-reference"] {
        let d = views.join(sub);
        std::fs::create_dir_all(&d)?;
        for entry in std::fs::read_dir(&d)?.flatten() {
            if entry.path().extension().is_some_and(|e| e == "md") {
                std::fs::remove_file(entry.path())?;
            }
        }
    }

    let mut buckets: BTreeMap<(&str, String), Vec<&Doc>> = BTreeMap::new();
    for d in &docs {
        for t in d.frontmatter.tags().unwrap_or_default() {
            buckets.entry(("by-tag", t)).or_default().push(d);
        }
        let status = d.status().unwrap_or("").to_string();
        buckets.entry(("status", status)).or_default().push(d);
        // A doc is listed under each (live) id it references.
        for (tid, val) in refs::parse(&d.frontmatter) {
            if !refs::is_struck(&val) {
                buckets.entry(("by-reference", tid)).or_default().push(d);
            }
        }
    }
    for ((sub, key), dl) in &buckets {
        let mut out = vec![
            format!("# {sub}: {key}"),
            String::new(),
            "Generated by opys — do not edit.".to_string(),
            String::new(),
        ];
        for d in dl {
            out.push(format!(
                "- {} [{}] {}",
                d.id().unwrap_or(""),
                d.status().unwrap_or(""),
                d.title
            ));
        }
        std::fs::write(
            views.join(sub).join(format!("{key}.md")),
            out.join("\n") + "\n",
        )?;
    }

    let count = walkdir::WalkDir::new(views)
        .into_iter()
        .filter_map(|e| e.ok())
        .filter(|e| e.file_type().is_file() && e.path().extension().is_some_and(|x| x == "md"))
        .count();
    Ok(count)
}

fn index_line(d: &Doc) -> String {
    let tags = d.frontmatter.tags().unwrap_or_default().join(", ");
    format!(
        "- {} [{}] ({}) {}",
        d.id().unwrap_or(""),
        d.status().unwrap_or(""),
        tags,
        d.title
    )
}
